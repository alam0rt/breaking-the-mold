/* CalculateStereoVolume @ 0x8007C818 (0x210). Stereo pan law: out[0]=left,
 * out[1]=right from (vol, pan 0..0x1E0); /160 slope = 0x66666667 magic-mul,
 * final /4 = reverb scale. SHELVED: m2c structure transcribed (see git
 * history / draft) compiles to 116 instrs vs target 132 — cc1 CSEs the
 * repeated `(s16)pan` sign-extends and `vol/160` divisions that the original
 * duplicates per branch. Needs the per-branch duplication forced (volatile /
 * re-read barriers) before the permuter can finish; 16-instr gap is too wide
 * for the permuter alone. */
INCLUDE_ASM("asm/nonmatchings/sound", CalculateStereoVolume);

extern void CalculateStereoVolume(s16 *out, s32 vol, s32 pan);
extern void SpuSetVoiceVolume(s32 voice, s16 volL, s16 volR);
extern u8 VOICE_SOUND_INDEX_TABLE[] asm("D_8009CC18");

extern SoundDefinition SOUND_DEFINITION_TABLE[] asm("D_8009CC68");

void SetVoicePanning(s32 voice_index, s16 pan_pos) {
    s16 vols[2];
    if ((u32)voice_index < 0x18) {
        CalculateStereoVolume(&vols[0],
                              SOUND_DEFINITION_TABLE[VOICE_SOUND_INDEX_TABLE[voice_index]].volume,
                              pan_pos);
        SpuSetVoiceVolume(voice_index, vols[0], vols[1]);
    }
}

/* Start CD-XA streaming track for a level/sub-stage combination.
 * Indexes a 2-byte-stride table at D_8009CFD8 by (scene*6 + (sub-1));
 * each entry is (track_id, channel). Validates scene<26 and 1<=sub<=6,
 * skips if the resolved track_id>=5 (only 5 CD-XA tracks available).
 * Marks CD streaming as active on success.
 *
 * Two keys to the match: (1) the single `&&` condition makes cc1 hoist the
 * `sub-1` computation into the first branch's delay slot like TARGET (a
 * separate-`if` form mis-fills it with `sw ra`); (2) computing `scene*6`
 * into its own temp before re-narrowing `(u8)t` for the index keeps cc1
 * from CSE-ing the bounds-check mask, so it re-emits the `andi`. */
void StartCDAudioForLevel(u8 scene, s32 sub) {
    s32 t = sub - 1;
    u8 ti;
    s32 s6;
    s32 idx;
    if (scene < 0x1A && (u8)t < 6) {
        s6 = scene * 6;
        ti = t;
        idx = (ti + s6) * 2;
        if (CD_LEVEL_TRACK_TABLE[idx] < 5) {
            PlayCDAudioTrack(CD_LEVEL_TRACK_TABLE[idx],
                             CD_LEVEL_CHANNEL_TABLE[idx]);
            CD_STREAM_ACTIVE = 1;
        }
    }
}

